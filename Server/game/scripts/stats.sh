#!/bin/bash
echo "-----------------------------------------------------------------------------"
ps au -A|head -1
if [ -f netrhost.pid ]
then
   ps au -A|grep $(cat netrhost.pid)|grep netrhost
elif [ -f ../netrhost.pid ]
then
   ps au -A|grep $(cat ../netrhost.pid)|grep netrhost
else
   ps au -A|grep netrhost
fi
echo "-----------------------------------------------------------------------------"
uptime
echo "-----------------------------------------------------------------------------"
free -l
echo "-----------------------------------------------------------------------------"
df -h .
echo "-----------------------------------------------------------------------------"
xx=$(quota -s)
if [ -n "$xx" ]
then
   echo "$xx"
   echo "-----------------------------------------------------------------------------"
fi
